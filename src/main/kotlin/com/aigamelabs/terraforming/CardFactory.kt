    fun getByName(name: String): Card {
        listOf(basic, corporate, promo, venus, colonies, prelude)
                .forEach { deck ->
                    val matches = deck.filter { it.name == name }
                    if (!matches.isEmpty)
                        return matches[0]
                }
        throw Exception("Card not found: $name")
    }

